// Array IndexOf e LastIndexOf
// Localizando elementos dentro de um array
// O indexOf localiza o índice de um determinado elemento
// LastIndexOf localiza o ultimo índice de um elemento

//indice -> 0  1  2   3   4   5 <-   Quero saber o indice do elemento 10.
let nums = [3, 6, 10, 15, 25, 78]; // Elementos -  3, 6, 10, 15, 25, 78

console.log(nums.indexOf(10));

console.log(nums.lastIndexOf(78));